interface I18nContextType {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
  dir: "rtl" | "ltr";
}

const I18nContext = createContext<I18nContextType>({
  lang: "he",
  setLang: () => {},
  t: (key) => key,
  dir: "rtl",
});

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLang] = useState<Lang>(() => {
    const stored = safeGetItem("bizaira_language");
    return stored === "en" || stored === "he" ? stored : "he";
  });

  useEffect(() => {
    safeSetItem("bizaira_language", lang);
    document.documentElement.dir = lang === "he" ? "rtl" : "ltr";
    document.body.dir = lang === "he" ? "rtl" : "ltr";
    document.documentElement.lang = lang;
  }, [lang]);

  const t = (key: string, params?: Record<string, string | number>): string => {
    let translation = translations[key]?.[lang] || key;
    if (params) {
      Object.entries(params).forEach(([param, value]) => {
        translation = translation.replace(new RegExp(`{{${param}}}`, "g"), String(value));
      });
    }
    return translation;
  };

  const dir = lang === "he" ? "rtl" : "ltr";

  return (
    <I18nContext.Provider value={{ lang, setLang, t, dir }}>
      <div dir={dir} lang={lang} className={`${lang === "en" ? "font-display text-left" : "font-heebo text-right"}`}>
        {children}
      </div>
    </I18nContext.Provider>
  );
};

export const useI18n = () => useContext(I18nContext);

export const LanguageToggle = () => {
  const { lang, setLang } = useI18n();

  return (
    <button
      onClick={() => setLang(lang === "he" ? "en" : "he")}
      className="bg-card border border-border px-4 py-2 rounded-lg text-xs font-bold text-foreground hover:scale-105 transition-all flex items-center gap-2 shadow-sm"
      style={{ fontFamily: "'Montserrat', sans-serif", borderColor: "hsl(217 20% 92%)", background: "hsl(36 33% 98%)" }}
      title={lang === "he" ? "Switch to English" : "עבור לעברית"}
    >
      <span style={{ color: lang === "he" ? "hsl(220 12% 45%)" : "#000810", fontWeight: lang === "he" ? 400 : 700 }}>EN</span>
      <span style={{ color: "hsl(220 12% 45%)" }}>/</span>
      <span style={{ color: lang === "he" ? "#000810" : "hsl(220 12% 45%)", fontWeight: lang === "he" ? 700 : 400 }}>עב</span>
    </button>
  );
};
